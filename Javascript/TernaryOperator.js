//Ternary Operator: condicion ? expresion1 : expresion2
//                  condicion: lo que se tiene que cumplir
//                              expresion1: si cumple con la condicion se imprime esto
//                                           expresion2: si no se cumple la condicion se imprime esto.
let price = 22;
let day ="Monday";

day === "Monday" ? price -= 1.5 : price += 1.5;